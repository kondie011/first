#include "libft.h"

t_vec3	vec3_cross(t_vec3 va, t_vec3 vb)
{
	t_vec3	v;

	v.x = va.y * vb.z - va.z * vb.y;
	v.y = va.z * vb.x - va.x * vb.z;
	v.z = va.x * vb.y - va.y * vb.x;
	return (v);
}
