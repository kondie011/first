#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int c;

	c = -1;
	while (s1[++c] != '\0' || s2[c] != '\0')
	{
		if (s1[c] - s2[c] != 0)
		{
			return (*(unsigned char*)(s1 + c) - *(unsigned char*)(s2 + c));
		}
	}
	return (0);
}
