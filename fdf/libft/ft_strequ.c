#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int c;

	c = -1;
	if (s1 && s2)
	{
		while (s1[++c])
			if (s1[c] != s2[c])
				return (0);
		if (s1[c] == '\0' && s2[c] == '\0')
			return (1);
	}
	return (0);
}
