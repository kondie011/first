#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int c;
	int i;

	c = -1;
	i = -1;
	while (s1[++c] != '\0')
		;
	while (++i < (int)n && s2[i] != '\0')
	{
		s1[c++] = s2[i];
	}
	s1[c] = '\0';
	return (s1);
}
